
binaryy = int(input("Enter a whole number "))
print(bin(binaryy))
print(f"An arbitrary number in octal: {oct(binaryy)}")
print(f"An arbitrary number in octal without a prefix: {binaryy:o}")
print(f"An arbitrary number in hexadecimal: {hex(binaryy)}")
print(f"An arbitrary number in hexadecimal without a prefix: {binaryy:x}")